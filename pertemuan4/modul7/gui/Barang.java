public class Barang {
    private String nama;
    private long harga, jumlah;
    public String getNama() {
        return nama;
    }
 
    public void setNama(String nama) {
        this.nama = nama;
    }
 
    public long getHarga() {
        return harga;
    }
 
    public void setHarga(long harga) {
        this.harga = harga;
    }
 
    public long getJumlah() {
        return jumlah;
    }
 
    public void setJumlah(long jumlah) {
        this.jumlah = jumlah;
    } 
}
